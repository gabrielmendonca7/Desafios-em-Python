# Cálculo de Área de um Retângulo:
# Desenvolva um programa que calcule a área de um retângulo.
# O programa deve ler a altura e a largura do retângulo e exibir o resultado.

altura = float(input("Digite a altura do retângulo: "))
largura = float(input("Digite a largura do retângulo: "))

area = altura * largura

print(f"Um retângulo de altura: {altura} e largura de: {largura}, gera uma área de {area}")